import path from 'node:path'
import fs from 'node:fs/promises'
import { globSync, readFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import type { CientosComponent, CientosPackage, PackageIndexes } from '../metadata/types'
import matter from 'gray-matter'

const __dirname = fileURLToPath(new URL('.', import.meta.url))
const DIR_ROOT = path.resolve(__dirname, '..')
const DIR_PLAYGROUND = path.resolve(DIR_ROOT, 'playground/vue/src')
const PATH_PLAYGROUND_ROUTER = path.resolve(DIR_PLAYGROUND, 'router.ts')
const DIR_SRC = path.resolve(DIR_ROOT, 'src')

export async function updateImport({ components }: PackageIndexes) {
  const DIR = 'src'
  const imports = [] as string[]
  const exports = [] as string[]

  for (const c of components) {
    if (c.component) {
      imports.push(`import ${c.name} from './${path.relative(DIR, c.component)}'`)
      exports.push(c.name)
      for (const alias of (c.alias ?? [])) { exports.push(`${c.name} as ${alias}`) }
    }
    if (c.fn) {
      const fnName = `use${c.name}`
      const pieces = path.parse(path.relative(DIR, c.fn))
      const filePath = `.${path.sep}${path.join(pieces.dir, pieces.name)}`
      imports.push(`import { ${fnName} } from '${filePath}'`)
      exports.push(fnName)
      for (const alias of (c.alias ?? [])) { exports.push(`${fnName} as use${alias}`) }
    }
  }

  const data = `// NOTE: Generated by ${path.relative('.', import.meta.filename)} – ${new Date(Date.now()).toLocaleString('en-GB')}
// Do not make edits here - they will be overwritten
${imports.sort().join('\n')}

export {
${exports.map(s => s.trim()).filter(s => !!s).sort().map(s => `  ${s},`).join('\n')}
}
`

  await fs.writeFile(path.join(DIR, 'index.ts'), data)
}

export async function updatePlaygroundRoutes({ components }: PackageIndexes) {
  const imports = [] as string[]
  const routes = [] as string[]
  const PLAYGROUND_PATH_RE = /.*playground(.*).vue$/

  let i = 0
  for (const c of components) {
    if (!c.playgrounds) { continue }
    for (const playground of c.playgrounds) {
      const [isMatch, suffix] = playground.match(PLAYGROUND_PATH_RE) ?? [null, '']
      if (!isMatch) { continue }
      const myPath = suffix ? path.sep + path.join(c.package, c.name, suffix) : path.sep + path.join(c.package, c.name)
      const name = suffix ? `${c.name} ${suffix}` : c.name
      const importedName = `P${String(i++).padStart(3, '0')}`

      imports.push(`import ${importedName} from './${path.relative(DIR_PLAYGROUND, playground)}'`)
      routes.push(
        `  {
    path: '${myPath}',
    name: '${name}',
    component: ${importedName},
    category: '${c.category}',
  }`,
      )
    }
  }

  const data = `// NOTE: Generated by ${path.relative('.', import.meta.filename)} – ${new Date(Date.now()).toLocaleString('en-GB')}
// Do not make edits here - they will be overwritten.
import { createRouter, createWebHistory } from 'vue-router'

import Home from './pages/index.vue'
${imports.join('\n')}

export const routes = [
  {
    path: '/',
    name: 'Home',
    component: Home,
  },
${routes.join(',\n')},
]

export const router = createRouter({
  history: createWebHistory(),
  routes,
})
`

  await fs.writeFile(PATH_PLAYGROUND_ROUTER, data)
}

